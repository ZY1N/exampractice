int     max(int* tab, unsigned int len)
{
    int m = 0;
    int i = 0;
    int flag = 0;

    while(i < len)
    {
        if (flag = 0)
        {
            m = tab[i];
            flag = 1;
        }
        else if (tab[i] > m)
            m = tab[i];
        i++;
    }
    return(m);
}
